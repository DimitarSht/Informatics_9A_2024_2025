#include<iostream>
using namespace std;
/*
4. Дадени са три реални променливи: A, B, C.
Ако са подредени в нарастващ ред, то да се удвоят;
в противен случай да се замени стойността на всяка
 променлива с противоположната стойност.
 Да се изведат новите стойности на A, B, C.
*/
int main()
{
 double A,B,C;
 cin>>A>>B>>C;
 if(B>A&&B<C&&C>A)
 {
     cout<<2*A<<" "<<2*B<<" "<<2*C;
 }
 else
 {
     cout<<-1*A<<-1*B<<-1*C;
 }
    return 0;
}

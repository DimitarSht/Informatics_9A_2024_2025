#include<iostream>
using namespace std;
int main()
{
    /// Как превръщаме едно цяло число в число с десетична запетая?
    int sum = 100;
    /// Превръщаме sum от 100 в 100.0 по следните начини:
    /// - (double)sum
    /// - sum *1.0
    /// Превръщането става само еднократно, тоест след 13-ти ред,
    /// ако изведем само cout<<sum<<endl; , ще се 
    /// изведе само 100
    cout << (double)sum << endl;
    return 0;
}
